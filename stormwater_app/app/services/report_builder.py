def _build_certification_table(body, meta):
    """
    Clean 3-row certification table.  The certification statement that was
    previously a standalone paragraph is now row 0, so the CERTIFICATION bar
    is immediately followed by this table with no intervening paragraph.

    Structure:
      Row 0  │ Certification statement — full-width merged, centered H+V
      Row 1  │ "Inspection Company:"   │ Company name + preparer + title
             │ "Report Prepared By:"  │ (cols 1+2 merged)
      Row 2  │ "Signature:"           │ blank space  │ Date
                                       ↑ right border here only

    insideV is disabled at table level; the single needed vertical divider
    (between sig-space and date in row 2) is added via _add_cell_right_border().
    """
    table = body.add_table(rows=3, cols=3)
    _apply_table_style_no_outer_vborders(table)
    _set_table_full_width(table)

    # Prevent any row of the cert table from splitting across a page boundary.
    # Combined with the conservative spacer in _build_cover_page, this ensures
    # the entire cert block stays together and on page 1.
    for row in table.rows:
        _set_row_no_split(row)

    col_widths = [Inches(2.0), Inches(4.0), Inches(1.5)]
    for row in table.rows:
        for ci, w in enumerate(col_widths):
            row.cells[ci].width = w

    cp      = _cl("table_cell_pad_pt", 1)
    body_sz = _cl("body_size_pt", STYLE_CONFIG["font_size_body"])

    # ── Row 0: Certification statement — full-width, centered ─────────────────
    cert_cell = table.cell(0, 0).merge(table.cell(0, 2))
    cert_p    = cert_cell.paragraphs[0]
    cert_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(cert_p, before=cp, after=cp)
    _style_run(cert_p.add_run(_CERT_TEXT), italic=True, size=body_sz)
    _center_cell_vertical(cert_cell)

    # ── Row 1: Inspection Company + Report Prepared By (combined) ─────────────
    # Merge value columns first, then populate both cells.
    merged_val = table.cell(1, 1).merge(table.cell(1, 2))

    lbl_cell = table.cell(1, 0)
    lp1 = lbl_cell.paragraphs[0]
    lp1.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(lp1, before=cp, after=0)
    _style_run(lp1.add_run("Inspection Company:"), bold=True, size=body_sz)
    lp2 = lbl_cell.add_paragraph()
    lp2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(lp2, before=0, after=cp)
    _style_run(lp2.add_run("Report Prepared By:"), bold=True, size=body_sz)
    _center_cell_vertical(lbl_cell)

    vp1 = merged_val.paragraphs[0]
    vp1.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(vp1, before=cp, after=0)
    _style_run(vp1.add_run(_COMPANY_NAME), size=body_sz)
    vp2 = merged_val.add_paragraph()
    vp2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(vp2, before=0, after=0)
    _style_run(vp2.add_run(meta.prepared_by or ""), size=body_sz)
    vp3 = merged_val.add_paragraph()
    vp3.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(vp3, before=0, after=cp)
    _style_run(vp3.add_run(_CERT_TITLE), italic=True, size=body_sz)
    _center_cell_vertical(merged_val)

    # ── Row 2: Signature | blank space | Date ─────────────────────────────────
    _cert_label_cell(table.cell(2, 0), "Signature:", cp, body_sz)
    _center_cell_vertical(table.cell(2, 0))

    sig_cell = table.cell(2, 1)
    sig_para = sig_cell.paragraphs[0]
    sig_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(sig_para,
                 before=_cl("cert_sig_before_pt", 12),
                 after=_cl("cert_sig_after_pt", 12))
    sig_para.add_run(" ")
    _center_cell_vertical(sig_cell)
    # Restore the single column divider between blank space and date
    _add_cell_right_border(sig_cell)

    date_cell = table.cell(2, 2)
    date_para = date_cell.paragraphs[0]
    date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_spacing(date_para, before=cp, after=cp)
    _style_run(date_para.add_run(f"Date: {meta.report_date or ''}"), size=body_sz)
    _center_cell_vertical(date_cell)


