def _estimate_photo_header_height_pt() -> float:
    """
    Conservative over-estimate of the photo page Row 0 height:
    logo paragraph + label paragraph + top black rule paragraph.
    """
    try:
        from PIL import Image as _PILImg
        img = _PILImg.open(str(_LOGO_PATH))
        w_px, h_px = img.size
        aspect = w_px / max(h_px, 1)
    except Exception:
        aspect = 3.296   # Sterling logo fallback
    logo_h   = (_PHOTO_LOGO_WIDTH / aspect) * 72.0 + 2.0   # image height + after=2
    label_h  = 10.0 * 1.20 + 2.0    # 10 pt bold line + before=2
    rule_h   = 8.5  * 1.20 + 4.0    # Normal-style para + after=4
    return logo_h + label_h + rule_h + 6.0   # +6 pt safety


def _build_photo_section(doc: Document, proj: ProjectSession, grid_type: str = "2x3"):
    """
    Continuous photo sheets across ALL systems.

    Photos from all systems are merged into one ordered stream (sorted by
    system position then display_order within each system), then batched
    into pages of `per_page` photos each.

    Each page uses a 3-row borderless layout table (same structural approach
    as the cover page):
      Row 0  fixed   logo header + label + top black rule
      Row 1  flex    photo grid  (exact height = remaining body space)
      Row 2  fixed   bottom black rule  (structurally anchored — no spacer)

    The bottom black rule is in Row 2 by construction; it never orphans onto
    a blank page regardless of how many photos are on the page.
    """
    cfg         = _GRID_CFG.get(grid_type, _GRID_CFG["2x3"])
    cols        = cfg["cols"]
    per_page    = cfg["per_page"]
    col_w       = cfg["col_w"]
    photo_w     = cfg["photo_w"]
    photo_h_max = cfg["photo_h_max"]
    rows_pp     = cfg["rows"]

    if not proj.photos:
        return

    # ── Build one flat ordered list: system order → display_order ────────────
    system_order = {s.entry_id: i for i, s in enumerate(proj.systems)}
    all_photos = sorted(
        proj.photos,
        key=lambda p: (system_order.get(p.system_entry_id, 9999), p.display_order),
    )

    # ── Batch across all photos continuously ─────────────────────────────────
    batches = [all_photos[i:i + per_page] for i in range(0, len(all_photos), per_page)]

    # Pre-compute photo page row heights (same for every batch).
    # Row 0: logo + label + top rule (fixed, over-estimated)
    # Row 2: bottom black rule (fixed, generous)
    # Row 1: remaining body space (absorbs everything else)
    _PHOTO_OVERHEAD_PT = 4.0   # table rendering overhead (no leading blank para on photo pages)
    _photo_row0_h = _estimate_photo_header_height_pt()
    _photo_row2_h = 18.0   # bottom rule: generous — actual ~14 pt, extra anchors it safely
    _photo_row1_h = max(10.0,
                        _BODY_HEIGHT_PT - _PHOTO_OVERHEAD_PT - _photo_row0_h - _photo_row2_h)

    print(f"[photo-table] row0={_photo_row0_h:.1f}  row1={_photo_row1_h:.1f}  "
          f"row2={_photo_row2_h:.1f}  total={_photo_row0_h+_photo_row1_h+_photo_row2_h:.1f}  "
          f"body={_BODY_HEIGHT_PT:.1f}")

    for batch in batches:
        doc.add_page_break()

        # ── Phase 1: build all photo-page content in flat doc flow ────────────
        body_el   = doc.element.body
        _snap0_set = set(body_el)   # snapshot AFTER page break (includes break para)

        # Row 0 content: logo + label + top rule
        _add_photo_page_logo_header(doc)
        lbl = doc.add_paragraph()
        _set_spacing(lbl, before=2, after=0)
        _style_run(lbl.add_run("Service Photos Are Provided Below:"), bold=True, size=10)
        _add_black_rule(doc)

        _snap1_list = list(body_el)
        _snap1_set  = set(_snap1_list)

        # Row 1 content: photo grid
        _build_photo_grid_table(doc, batch, cols, col_w, photo_w, photo_h_max, rows_pp)

        _snap2_list = list(body_el)
        _snap2_set  = set(_snap2_list)

        # Row 2 content: bottom black rule
        _add_black_rule(doc)

        _snap3_list = list(body_el)

        # ── Phase 2: classify elements by destination row ─────────────────────
        row0_elems = [e for e in _snap1_list if e not in _snap0_set]
        row1_elems = [e for e in _snap2_list if e not in _snap1_set]
        row2_elems = [e for e in _snap3_list if e not in _snap2_set]

        # ── Phase 3: remove classified elements from body ─────────────────────
        for elem in row0_elems + row1_elems + row2_elems:
            body_el.remove(elem)

        # ── Phase 4: create 3-row borderless outer layout table ───────────────
        outer = doc.add_table(rows=3, cols=1)
        _remove_table_borders(outer)
        _set_table_full_width(outer)
        _zero_table_cell_spacing(outer)

        for row, h in [(outer.rows[0], _photo_row0_h),
                       (outer.rows[1], _photo_row1_h),
                       (outer.rows[2], _photo_row2_h)]:
            _set_row_min_height(row, h, rule="exact")
            _zero_all_cell_margins(row.cells[0])

        # ── Phase 5: inject content into cells ────────────────────────────────
        _inject_into_cell(outer.rows[0].cells[0], row0_elems)
        _inject_into_cell(outer.rows[1].cells[0], row1_elems)
        _inject_into_cell(outer.rows[2].cells[0], row2_elems)


