def _build_cover_page(doc: Document, proj: ProjectSession):
    """
    3-zone layout using a full-body-height borderless outer table:

      Row 0  fixed   logo + title + GENERAL INFORMATION bar + info table
      Row 1  flex    INTRODUCTION bar + intro text + FINDINGS bar + write-ups
      Row 2  fixed   CERTIFICATION bar + cert table + bottom green bar

    Row heights sum to exactly _BODY_HEIGHT_PT so Row 2 (cert) is always
    anchored at the body bottom — no calibration constant needed.

    WHY FLAT-FLOW BUILD THEN XML REARRANGE:
      python-docx Cell.add_table() can scramble paragraph order inside a cell
      because add_p_before() searches ALL descendants for the last <w:p>,
      sometimes landing inside a nested table instead of at the cell level.
      Building everything in flat document flow (where Document.add_table()
      has no ordering bug) and then moving the XML elements into table cells
      sidesteps the issue entirely.  Image relationships stay valid because
      the content never leaves the same Document part.
    """
    body_el = doc.element.body

    # ── Phase 1: build all content in flat doc flow ────────────────────────────
    # Snapshot which elements exist BEFORE we start (typically the blank
    # initial <w:p> and the <w:sectPr> that python-docx puts into every new
    # Document body).
    #
    # IMPORTANT: use lxml element identity (set of elements), NOT id().
    # lxml wraps C-level xmlNode objects in transient Python objects; id()
    # returns the Python wrapper's memory address, which can be recycled by
    # the garbage collector after the wrapper is released.  Using a set of
    # the element objects themselves relies on lxml's __hash__ / __eq__,
    # both of which compare the stable underlying C-pointer, so the identity
    # survives across multiple list() calls and GC cycles.
    _pre_existing = set(body_el)   # ← holds refs → wrappers stay alive too

    _build_cover_row1_content(doc, proj)
    _snap0     = list(body_el)
    _snap0_set = set(_snap0)       # ← same: keep refs alive

    _build_cover_row2_content(doc, proj)
    _snap1     = list(body_el)
    _snap1_set = set(_snap1)

    _build_cert_section(doc, proj.meta)
    _snap2 = list(body_el)

    # ── Phase 2: classify elements by destination row ─────────────────────────
    row0_elems = [e for e in _snap0 if e not in _pre_existing]
    row1_elems = [e for e in _snap1 if e not in _snap0_set]
    row2_elems = [e for e in _snap2 if e not in _snap1_set]

    # ── Phase 3: compute exact row heights ────────────────────────────────────
    # _TABLE_OVERHEAD_PT: budget for (a) the 1-pt leading paragraph left in
    # body by Phase 4, (b) Word's table rendering overhead, (c) twip-rounding.
    # 8 pt provides ~7 pt of buffer beyond the 1-pt blank para.
    _TABLE_OVERHEAD_PT = 8.0
    row0_h = _estimate_row1_height_pt(proj)
    row2_h = _estimate_cert_height_pt()        # conservative — includes safety margin
    row1_h = max(20.0, _BODY_HEIGHT_PT - _TABLE_OVERHEAD_PT - row0_h - row2_h)

    print(f"[cover-table] row0={row0_h:.1f}  row1={row1_h:.1f}  "
          f"row2={row2_h:.1f}  total={row0_h + row1_h + row2_h:.1f}  "
          f"body={_BODY_HEIGHT_PT:.1f}  overhead={_TABLE_OVERHEAD_PT:.1f}")

    # ── Phase 4: remove content elements from body ────────────────────────────
    for elem in row0_elems + row1_elems + row2_elems:
        body_el.remove(elem)

    # Minimize any pre-existing blank paragraphs to 1 pt exact height instead
    # of removing them.  Removal risks destroying the <w:sectPr> node (which
    # may be embedded in a <w:p> depending on the python-docx template version),
    # leaving doc.sections[] empty and crashing doc.add_table().  A 1-pt
    # minimized paragraph is visually invisible and is already budgeted for
    # in _TABLE_OVERHEAD_PT above.
    #
    # CRITICAL: We must also collapse the paragraph's font to 1 pt.
    # Word enforces a minimum line height equal to the rendered font ascender +
    # descender, so a paragraph whose Normal style specifies 8.5 pt Calibri will
    # render at ~8–9 pt even when w:lineRule="exact" w:line="20" is set.
    # Without the font collapse the blank para consumes ~8 pt, not 1 pt, and the
    # outer table total overshoots the body height by ~4–5 pt — enough to push
    # the Certification row (Row 2) onto page 2.
    for elem in list(body_el):
        if elem in _pre_existing and elem.tag == qn("w:p"):
            has_text = bool(elem.findall(f".//{qn('w:t')}"))
            if not has_text:
                _pPr = elem.get_or_add_pPr()
                # ── spacing: exact 1 pt line, no before/after ────────────────
                for _sp in list(_pPr):
                    if _sp.tag == qn("w:spacing"):
                        _pPr.remove(_sp)
                _sp = OxmlElement("w:spacing")
                _sp.set(qn("w:before"),   "0")
                _sp.set(qn("w:after"),    "0")
                _sp.set(qn("w:line"),     "20")   # 20 twips = 1 pt
                _sp.set(qn("w:lineRule"), "exact")
                _pPr.append(_sp)
                # ── font: collapse to 1 pt so Word's minimum-height floor ────
                # doesn't expand the line beyond what we declared above.
                _rPr = _pPr.find(qn("w:rPr"))
                if _rPr is None:
                    _rPr = OxmlElement("w:rPr")
                    _pPr.append(_rPr)
                for _tag in ("w:sz", "w:szCs"):
                    _existing_sz = _rPr.find(qn(_tag))
                    if _existing_sz is not None:
                        _rPr.remove(_existing_sz)
                    _sz = OxmlElement(_tag)
                    _sz.set(qn("w:val"), "2")   # 2 half-points = 1 pt
                    _rPr.append(_sz)

    # ── Phase 5: create borderless outer layout table ─────────────────────────
    # Phase 4's guard preserved <w:sectPr> in body, so doc.sections[-1] is
    # intact and doc.add_table() can safely read _block_width from it.
    outer = doc.add_table(rows=3, cols=1)
    _remove_table_borders(outer)
    _set_table_full_width(outer)
    # Explicitly zero table-level cell spacing (w:tblCellSpacing) so the
    # inherited table style does not add hidden inter-row height.
    _zero_table_cell_spacing(outer)

    for row, h in [(outer.rows[0], row0_h),
                   (outer.rows[1], row1_h),
                   (outer.rows[2], row2_h)]:
        _set_row_min_height(row, h, rule="exact")
        _zero_all_cell_margins(row.cells[0])

    # Row 2 cert cell: anchor content to the BOTTOM of the cell.
    # row2_h is intentionally larger than the actual cert content to guarantee
    # it always fits.  vAlign="bottom" places cert at the cell bottom so the
    # surplus white space appears ABOVE cert (between row 1 and cert), not
    # below cert (which would push it away from the footer boundary).
    _cert_tc   = outer.rows[2].cells[0]._tc
    _cert_tcPr = _cert_tc.get_or_add_tcPr()
    _va_existing = _cert_tcPr.find(qn("w:vAlign"))
    if _va_existing is not None:
        _cert_tcPr.remove(_va_existing)
    _va = OxmlElement("w:vAlign")
    _va.set(qn("w:val"), "bottom")
    _cert_tcPr.append(_va)

    # ── Phase 6: inject content into cells ────────────────────────────────────
    _inject_into_cell(outer.rows[0].cells[0], row0_elems)
    _inject_into_cell(outer.rows[1].cells[0], row1_elems)
    _inject_into_cell(outer.rows[2].cells[0], row2_elems)

    # ── Phase 7: collapse trailing body paragraph ─────────────────────────────
    _minimize_trailing_para(doc)


