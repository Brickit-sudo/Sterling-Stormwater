# ── Cert bottom-alignment ─────────────────────────────────────────────────────

def _align_cert_to_bottom(doc: Document):
    """
    Insert a vertical spacer before the CERTIFICATION section so that it
    lands at the bottom of page 1.

    Strategy:
      - The page-fit algorithm already estimated total content height (used_pt)
        and the available printable height (available_pt).
      - If the content fits (used_pt <= available_pt), the difference is free
        white space.  Inserting a paragraph with space_before equal to that
        difference pushes CERTIFICATION to the foot of the page.
      - If the content overflows, no spacer is added (cert is as low as possible).
      - A 6 pt minimum threshold avoids adding a spacer for trivial amounts.
    """
    if _COVER_FIT_RESULT is None:
        return
    spacer_pt = _COVER_FIT_RESULT.available_pt - _COVER_FIT_RESULT.used_pt
    if spacer_pt > 6.0:
        p = doc.add_paragraph()
        p.paragraph_format.space_before = Pt(spacer_pt)
        p.paragraph_format.space_after  = Pt(0)
        p.add_run(" ")


