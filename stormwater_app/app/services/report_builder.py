def _inject_into_cell(cell, elems):
    """
    Move lxml elements into a table cell, inserting them before the trailing
    mandatory <w:p> that python-docx puts in every new cell.

    Shared by both _build_cover_page (Phase 6) and _build_photo_section
    (Phase 5) so the XML-rearrange pattern is implemented in one place.
    """
    tc = cell._tc
    # Find the last direct-child <w:p> — that is the mandatory trailing para.
    last_p = None
    for child in tc:
        if child.tag == qn("w:p"):
            last_p = child
    for elem in elems:
        if last_p is not None:
            tc.insert(list(tc).index(last_p), elem)
        else:
            tc.append(elem)


