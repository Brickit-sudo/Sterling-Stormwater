def _bmp_summary_lines(proj: ProjectSession) -> list:
    lines  = []
    by_type = defaultdict(list)
    for s in proj.systems:
        if s.system_type:
            by_type[s.system_type].append(s)
    for stype, entries in by_type.items():
        count = len(entries)
        ids   = [e.system_id for e in entries if e.system_id]
        if ids and count == 1:
            lines.append(f"({count}) {stype} (BMP# {ids[0]})")
        elif ids:
            lines.append(f"({count}) {stype} (BMP# {', '.join(ids)})")
        else:
            lines.append(f"({count}) {stype}")
    return lines


