def _configure_document(doc: Document):
    side   = Inches(STYLE_CONFIG["page_margin_side_inches"])
    top    = Inches(STYLE_CONFIG["page_margin_top_inches"])
    bottom = Inches(STYLE_CONFIG["page_margin_bottom_inches"])
    for section in doc.sections:
        section.top_margin    = top
        section.bottom_margin = bottom
        section.left_margin   = side
        section.right_margin  = side

    style = doc.styles["Normal"]
    style.font.name = STYLE_CONFIG["font_body"]
    style.font.size = Pt(STYLE_CONFIG["font_size_body"])


