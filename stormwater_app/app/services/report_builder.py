def build_report(proj: ProjectSession, filename: str,
                 template_path: str = "",
                 photo_grid: str = "2x3") -> str:
    """
    Build the Sterling DOCX report and write it to OUTPUT_DIR/filename.

    Args:
        proj:          ProjectSession with all report data.
        filename:      Output filename (saved to OUTPUT_DIR).
        template_path: Unused in v2 (kept for API compatibility).
        photo_grid:    Grid layout — "2x2", "2x3" (default), or "3x3".

    Returns absolute path of saved file.
    """
    OUTPUT_DIR.mkdir(exist_ok=True)
    # Validate grid; fall back to default
    if photo_grid not in _GRID_CFG:
        photo_grid = "2x3"

    # ── Run page-fit algorithm and populate cover layout ──────────────────────
    global _COVER_FIT_RESULT
    fit_result = apply_page_fit(proj)
    _COVER_FIT_RESULT = fit_result
    L = fit_result.layout
    _COVER_LAYOUT.update({
        "body_size_pt":       L.body_size_pt,
        "logo_width_in":      L.logo_width_in,
        "logo_after_pt":      L.logo_after_pt,
        "title_size_pt":      L.title_size_pt,
        "title_before_pt":    L.title_before_pt,
        "title_after_pt":     L.title_after_pt,
        "bar_before_pt":      L.bar_before_pt,
        "bar_after_pt":       L.bar_after_pt,
        "bar_spacer_pt":      L.bar_spacer_pt,
        "body_before_pt":     L.body_before_pt,
        "body_after_pt":      L.body_after_pt,
        "subhead_before_pt":  L.subhead_before_pt,
        "subhead_after_pt":   L.subhead_after_pt,
        "table_cell_pad_pt":  L.table_cell_pad_pt,
        "cert_sig_before_pt": L.cert_sig_before_pt,
        "cert_sig_after_pt":  L.cert_sig_after_pt,
        "intro_after_pt":     L.intro_after_pt,
        # Page-count metadata for the cover table
        "photo_grid":  photo_grid,
        "n_photos":    len(proj.photos),
    })
    # Optionally surface fit diagnostics in the log
    import logging
    _log = logging.getLogger(__name__)
    _log.debug(fit_result.debug_summary())
    # ── Diagnostic: print page-fit results to terminal so we can calibrate ────
    print(fit_result.debug_summary())

    doc = Document()
    _configure_document(doc)

    _build_cover_page(doc, proj)
    _add_footer(doc.sections[0])

    # NOTE: Interior body pages (_build_inspection_body etc.) are intentionally
    # NOT called here.  The cover page already contains all findings content —
    # calling them again would duplicate every findings section.  The functions
    # are kept in this file for reference but are not part of the build flow.

    if proj.photos:
        _build_photo_section(doc, proj, grid_type=photo_grid)

    for section in doc.sections[1:]:
        _add_footer(section)

    out_path = OUTPUT_DIR / filename
    doc.save(str(out_path))
    return str(out_path)


