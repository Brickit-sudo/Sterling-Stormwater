def _estimate_row1_height_pt(proj: ProjectSession) -> float:
    """
    Estimate height of outer-table Row 0 (logo + title + GENERAL INFORMATION + info table).
    Intentionally a slight over-estimate so content is never clipped; any slack
    appears as invisible white space at the bottom of Row 0.
    """
    # Logo
    try:
        from PIL import Image as _PILImg
        img    = _PILImg.open(str(_LOGO_PATH))
        w_px, h_px = img.size
        aspect = w_px / max(h_px, 1)
    except Exception:
        aspect = 3.296
    logo_w = _cl("logo_width_in", _COVER_LOGO_WIDTH)
    logo_h = (logo_w / aspect) * 72.0 + _cl("logo_after_pt", 4)

    # Title
    title_sz = _cl("title_size_pt", 14)
    title_h  = title_sz * 1.2 + _cl("title_before_pt", 2) + _cl("title_after_pt", 6)

    # GENERAL INFORMATION bar
    bar_h = 14.0

    # Cover info table (3 rows, min-height driven by right-column BMP lines)
    body_sz    = _cl("body_size_pt", STYLE_CONFIG["font_size_body"])
    cp         = _cl("table_cell_pad_pt", 1)
    line_h     = body_sz * 1.2 + 2 * cp
    bmp_lines  = _bmp_summary_lines(proj)
    right_lines = 1 + len(bmp_lines)
    row_min    = max(18.0, (right_lines * line_h) / 3)
    table_h    = 3 * row_min

    return logo_h + title_h + bar_h + table_h + 4.0   # +4 pt safety


def _estimate_cert_height_pt() -> float:
    """
    Conservative over-estimate of outer-table Row 2 height.

    Actual content (measured from _build_cert_section / _build_certification_table):
      CERTIFICATION bar          ≈ 14 pt  (10pt text × 1.2 lhf, zero cell padding)
      cert table row 0 (text)    ≈ 22 pt  (2 lines × line_h + 2×cp)
      cert table row 1 (names)   ≈ 33 pt  (3 lines × line_h + 2×cp)
      cert table row 2 (sig)     ≈ 34 pt  (line_h + sig_before + sig_after)
      cert table border overhead ≈  6 pt  (_apply_table_style_no_outer_vborders
                                            adds top/insideH×2/bottom borders at
                                            sz=12; each border ≈ 1.5 pt rendered)
      bottom green bar           ≈ 16 pt  (before=2, line, after=2)
    Raw sub-total                ≈ 125 pt

    Safety margin                  23 pt  — absorbs Word rendering variance,
                                            any paragraph spacing that differs
                                            from our model, and cell-margin
                                            defaults inherited from the table style.

    Row 2's cell uses w:vAlign="bottom" so cert content anchors to the cell
    bottom even when row2_h > actual cert height; surplus space appears ABOVE
    cert (inside row 2 cell), which is invisible white space.
    """
    body_sz = _cl("body_size_pt", STYLE_CONFIG["font_size_body"])
    cp      = _cl("table_cell_pad_pt", 1)
    line_h  = body_sz * 1.2

    cert_bar_h = 14.0                              # CERTIFICATION green bar

    row0_h = 2 * line_h + 2 * cp                  # cert statement — 2 lines
    row1_h = 3 * line_h + 2 * cp                  # company/preparer/title — 3 lines
    sig_before = _cl("cert_sig_before_pt", 12)
    sig_after  = _cl("cert_sig_after_pt",  12)
    row2_h = line_h + sig_before + sig_after        # signature row

    border_overhead = 6.0                          # top + 2×insideH + bottom borders
    cert_table_h    = row0_h + row1_h + row2_h + border_overhead

    green_bar_h  = 16.0                            # bottom green bar

    raw_estimate  = cert_bar_h + cert_table_h + green_bar_h
    safety_margin = 23.0
    return raw_estimate + safety_margin


